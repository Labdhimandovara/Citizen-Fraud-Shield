class LanguageManager:
    """Manage multilingual support"""
    
    def __init__(self):
        self.languages = LANGUAGE_TRANSLATIONS
    
    def get_text(self, key: str, language: str = "en") -> str:
        """Get translated text"""
        if language not in self.languages:
            language = "en"
        
        return self.languages[language]["translations"].get(key, key)
    
    def get_all_languages(self) -> dict:
        """Get all available languages"""
        return {code: data["name"] for code, data in self.languages.items()}
    
    def get_language_flag(self, language: str) -> str:
        """Get language flag emoji"""
        return self.languages.get(language, {}).get("flag", "🌐")

class MultilingualTranslator(LanguageManager):
    """Translation adapter used by the FastAPI application."""

    def get_supported_languages(self) -> dict:
        """Return metadata in the shape expected by the languages endpoint."""
        return {
            code: {
                "name": data["name"],
                "native": data["name"],
                "flag": data.get("flag", "🌐"),
            }
            for code, data in self.languages.items()
        }

    def translate_result(self, result: dict, language: str) -> dict:
        """Translate known catalogue text while preserving dynamic API data."""
        if language not in self.languages or language == "en":
            return result

        english = self.languages["en"]["translations"]
        target = self.languages[language]["translations"]
        translated_values = {
            source: target.get(key, source) for key, source in english.items()
        }

        def translate(value):
            if isinstance(value, dict):
                return {key: translate(item) for key, item in value.items()}
            if isinstance(value, list):
                return [translate(item) for item in value]
            if isinstance(value, str):
                return translated_values.get(value, value)
            return value

        return translate(result)


